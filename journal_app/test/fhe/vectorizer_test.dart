// ── Helpers ────────────────────────────────────────────────────────────────

/// Absolute tolerance for float32 comparison.
///
/// The Python pipeline operates in float64 internally but exports assets as
/// float32.  The Dart implementation accumulates float32 rounding errors across
/// ~50 dot-product terms.  2e-4 is generous enough to cover this while still
/// catching real bugs.
const double _kAbsTol = 2e-4;

/// Relative tolerance (used for values whose magnitude exceeds 0.01).
/// Relaxed from 1% to 2% to accommodate expected float32 drift between
/// the Dart implementation (float32 accumulation) and Python reference (float64).
const double _kRelTol = 0.02; // 2%

void _expectClose(double dart, double python, String context) {
  final absDiff = (dart - python).abs();
  final relDiff = python.abs() > 1e-6 ? absDiff / python.abs() : absDiff;
  if (absDiff > _kAbsTol && relDiff > _kRelTol) {
    fail('$context: dart=$dart python=$python '
        'absDiff=${absDiff.toStringAsExponential(2)} '
        'relDiff=${(relDiff * 100).toStringAsFixed(2)}%');
  }
}

// ── Tests ──────────────────────────────────────────────────────────────────

void main() {
  // Required to load Flutter assets (rootBundle) in tests.
  TestWidgetsFlutterBinding.ensureInitialized();

  late Vectorizer vectorizer;

  setUpAll(() async {
    vectorizer = Vectorizer();
    await vectorizer.load();
  });

  group('Vectorizer.transform — output matches Python reference', () {
    for (final entry in _kRefVectors.entries) {
      final sentence = entry.key;
      final refFirst10 = entry.value;

      test(sentence, () {
        final result = vectorizer.transform(sentence);

        expect(result.length, equals(50),
            reason: 'Output must have exactly 50 dimensions (LSA components)');

        // Compare first 10 dimensions against Python reference.
        for (int i = 0; i < refFirst10.length; i++) {
          _expectClose(
            result[i].toDouble(),
            refFirst10[i],
            'dim[$i] for "$sentence"',
          );
        }
      });
    }
  });

  group('Vectorizer.transform — output is L2-normalised', () {
    for (final sentence in _kRefVectors.keys) {
      test(sentence, () {
        final result = vectorizer.transform(sentence);
        double norm = 0;
        for (final v in result) {
          norm += v * v;
        }
        norm = math.sqrt(norm);
        expect(norm, closeTo(1.0, 5e-4),
            reason: 'Output vector must be unit-length (L2 norm ≈ 1)');
      });
    }
  });

  group('Vectorizer.transform — deterministic (same input → same output)', () {
    for (final sentence in _kRefVectors.keys) {
      test(sentence, () {
        final a = vectorizer.transform(sentence);
        final b = vectorizer.transform(sentence);
        for (int i = 0; i < a.length; i++) {
          expect(a[i], equals(b[i]),
              reason: 'transform() must be deterministic (dim $i differs)');
        }
      });
    }
  });

  group('Vectorizer.transform — empty / OOV input', () {
    test('empty string returns zero vector or unit vector', () {
      // Empty input: all counts are 0, tfidf is all-zero → L2 norm guard returns
      // zero vector before SVD.  Final output is either all-zero or unit-length
      // depending on whether SVD output also happens to be zero.
      final result = vectorizer.transform('');
      expect(result.length, equals(50));
      // Should not throw and should produce finite values.
      for (final v in result) {
        expect(v.isFinite, isTrue, reason: 'All values must be finite');
      }
    });

    test('out-of-vocabulary tokens produce non-NaN output', () {
      // Tokens not in vocab are skipped; result is zero → guard handles it.
      final result = vectorizer.transform('xyzzy qqqqqq aaabbbccc');
      expect(result.length, equals(50));
      for (final v in result) {
        expect(v.isNaN, isFalse, reason: 'NaN values are not allowed');
        expect(v.isInfinite, isFalse, reason: 'Infinite values are not allowed');
      }
    });

    test('single character tokens are ignored (min length 2)', () {
      // "a b c" — all single-char tokens should be skipped.
      final allSingleChar = vectorizer.transform('a b c');
      final empty = vectorizer.transform('');
      // Both should produce identical output since no vocab terms match.
      for (int i = 0; i < 50; i++) {
        expect(allSingleChar[i], equals(empty[i]),
            reason: 'Single-char tokens must be filtered out (dim $i)');
      }
    });
  });

  group('Vectorizer.transform — case insensitivity', () {
    test('upper/lower/mixed case produce the same result', () {
      final lower = vectorizer.transform('i am happy');
      final upper = vectorizer.transform('I AM HAPPY');
      final mixed = vectorizer.transform('I Am Happy');
      for (int i = 0; i < 50; i++) {
        expect(lower[i], equals(upper[i]),
            reason: 'lower vs upper: dim $i differs');
        expect(lower[i], equals(mixed[i]),
            reason: 'lower vs mixed: dim $i differs');
      }
    });
  });
}
