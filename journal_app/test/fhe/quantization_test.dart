// ── Tests ──────────────────────────────────────────────────────────────────

void main() {
  // Load quantization params once for all tests.
  late List<Map<String, dynamic>> inputQuantizers;
  late Map<String, dynamic> outputQuantizer;

  setUpAll(() {
    final zipBytes = File('../journal_app/assets/fhe/client.zip').readAsBytesSync();
    final parseResult = ClientZipParser.parse(zipBytes);
    final qp = parseResult.quantParams;
    inputQuantizers = qp.input.map((p) => {
      'scale': p.scale,
      'zero_point': p.zeroPoint,
    }).toList();
    outputQuantizer = {
      'scale': qp.output.scale,
      'zero_point': qp.output.zeroPoint,
      'offset': qp.output.offset,
      'n_classes': 5,
    };
  });

  group('client.zip quantization sanity checks', () {
    test('has exactly 200 input quantizers', () {
      expect(inputQuantizers.length, equals(200));
    });

    test('each input quantizer has required fields', () {
      for (int i = 0; i < inputQuantizers.length; i++) {
        final q = inputQuantizers[i];
        expect(q.containsKey('scale'), isTrue, reason: 'input[$i] missing scale');
        expect(q.containsKey('zero_point'), isTrue, reason: 'input[$i] missing zero_point');
        expect((q['scale'] as num).toDouble(), greaterThan(0),
            reason: 'input[$i] scale must be positive');
      }
    });

    test('output quantizer has required fields', () {
      expect(outputQuantizer.containsKey('scale'), isTrue);
      expect(outputQuantizer.containsKey('zero_point'), isTrue);
      expect(outputQuantizer.containsKey('n_classes'), isTrue);
      expect(outputQuantizer['n_classes'], equals(5));
    });

    test('all input zero_points are in [0, 255]', () {
      for (int i = 0; i < inputQuantizers.length; i++) {
        final zp = inputQuantizers[i]['zero_point'] as int;
        expect(zp, inInclusiveRange(0, 255),
            reason: 'input[$i] zero_point out of range');
      }
    });
  });

  group('quantizeInput formula', () {
    test('basic examples match expected values', () {
      // x=0, scale=1, zp=0 → 0
      expect(quantizeInput(0.0, 1.0, 0), equals(0));
      // x=1, scale=1, zp=0 → 1
      expect(quantizeInput(1.0, 1.0, 0), equals(1));
      // x=0.5, scale=1, zp=0 → round(0.5)=0 (banker's rounding) or 1 — just finite
      expect(quantizeInput(0.5, 1.0, 0), inInclusiveRange(0, 1));
      // x=-1, scale=1, zp=128 → clip(round(-1)+128, 0, 255) = 127
      expect(quantizeInput(-1.0, 1.0, 128), equals(127));
      // x=200, scale=1, zp=0 → clip(200, 0, 255) = 200
      expect(quantizeInput(200.0, 1.0, 0), equals(200));
    });

    test('clamps to [0, 255]', () {
      expect(quantizeInput(1000.0, 1.0, 0), equals(255));
      expect(quantizeInput(-1000.0, 1.0, 0), equals(0));
    });

    test('scale divides correctly', () {
      // x=0.5, scale=0.5, zp=0 → round(1.0)+0 = 1
      expect(quantizeInput(0.5, 0.5, 0), equals(1));
      // x=1.0, scale=0.25, zp=10 → round(4)+10 = 14
      expect(quantizeInput(1.0, 0.25, 10), equals(14));
    });
  });

  group('dequantizeOutput formula', () {
    test('zero maps to -zero_point * scale', () {
      final scale = (outputQuantizer['scale'] as num).toDouble();
      final zp = outputQuantizer['zero_point'] as int;
      final result = dequantizeOutput(0, scale, zp);
      expect(result, closeTo(-zp * scale, 1e-10));
    });

    test('zero_point maps to 0.0', () {
      final scale = (outputQuantizer['scale'] as num).toDouble();
      final zp = outputQuantizer['zero_point'] as int;
      final result = dequantizeOutput(zp, scale, zp);
      expect(result, closeTo(0.0, 1e-10));
    });

    test('known output values are reasonable floats', () {
      final scale = (outputQuantizer['scale'] as num).toDouble();
      final zp = outputQuantizer['zero_point'] as int;
      // All uint8 values should dequantize to finite floats.
      for (int i = 0; i <= 255; i++) {
        final v = dequantizeOutput(i, scale, zp);
        expect(v.isFinite, isTrue, reason: 'dequantize($i) must be finite');
      }
    });
  });

  group('Input quantization matches Python reference (first 10 dims)', () {
    for (final entry in _kRefQuantized.entries) {
      final sentence = entry.key;
      final refQuantized = entry.value;
      final refVector = _kRefVectors[sentence]!;

      test(sentence, () {
        for (int i = 0; i < refQuantized.length; i++) {
          final q = inputQuantizers[i];
          final scale = (q['scale'] as num).toDouble();
          final zp = q['zero_point'] as int;
          final dartQ = quantizeInput(refVector[i], scale, zp);
          expect(dartQ, equals(refQuantized[i]),
              reason: 'dim[$i] for "$sentence": '
                  'dart=$dartQ python=${refQuantized[i]} '
                  '(x=${refVector[i]}, scale=$scale, zp=$zp)');
        }
      });
    }
  });
}
