// ── Quantization helpers (mirrors fhe_helper.py logic) ─────────────────────

/// Quantize a float32 value using an unsigned 8-bit uniform quantizer.
///
/// Formula: q = clip(round(x / scale) + zero_point, 0, 255)
/// Matches concrete-ml's UniformQuantizer (unsigned, n_bits=8).
int quantizeInput(double x, double scale, int zeroPoint) {
  final q = (x / scale).roundToDouble() + zeroPoint;
  return q.clamp(0, 255).toInt();
}

/// Dequantize a raw signed 8-bit output value to float32.
///
/// Formula: float = (int_val - zero_point) * scale
/// Matches concrete-ml's output quantizer (is_signed=True, offset=128).
double dequantizeOutput(int intVal, double scale, int zeroPoint) {
  return (intVal - zeroPoint) * scale;
}

